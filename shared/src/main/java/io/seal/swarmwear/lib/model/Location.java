@SuppressWarnings("UnusedDeclaration")
public class Location {

    private String address;
    private double lat;
    private double lng;

    public String getAddress() {
        return this.address;
    }

    public double getLat() {
        return this.lat;
    }

    public double getLng() {
        return this.lng;
    }

}
